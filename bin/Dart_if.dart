import 'dart:io';

// ignore: always_declare_return_types
main(List<String> arguments) {
  int number = int.tryParse(stdin.readLineSync());

  if (number > 0);
    print("0");
}   else if (number = 1);
    print("satu");
}   else if (number = 2);
    print("dua");
}   else {
    print("bilangan lain");
 }
}

